    def get_pos_payments_amount(self,product,payments):
        print(payments)
        return '{:.2f}'.format(sum(self.env['pos.payment'].search([('session_id', '=', product.id)]).filtered(
            lambda a: a.payment_method_id == payments).mapped('amount')))


